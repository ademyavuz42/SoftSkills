package Day02;

class DataType{
    public static void main(String[] args) {
        // Integer
        int numberInteger = 75;
        System.out.println("numberInteger : "+numberInteger);

        //Long
        long numberLong = 75;
        System.out.println("numberLong : "+numberLong);

        // short
        short numberShort = 75;
        System.out.println("Short : "+numberShort);

        // byte
        byte numberByte = 75;
        System.out.println("Byte : "+numberByte);

        //float
        float numberFloat = 75f;
        System.out.println("Float : " + numberFloat);

        // double
        double numberDouble =75;
        System.out.println("Double : " + numberDouble);

        // char
        char textChar = 'A';
        System.out.println("Char : " + textChar);

        // String
        String textString = "Adem";
        System.out.println("String : " + textString);

        // boolean
        boolean statu = true;
        System.out.println("Boolean : " + statu);

    }

}
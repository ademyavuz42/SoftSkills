package Day04;

public class UnaryOperators {
    public static void main(String[] args) {
        //Unary operators
        int number = 7;
        System.out.println(number);     // print number
        System.out.println(number++);   // post increment
        System.out.println(++number);   // pre increment
        System.out.println(number--);   // post decrement
        System.out.println(--number);   // pre decrement
    }
}

package Day04;

public class ComparisionOperators {
    public static void main(String[] args) {
        // Comparison Operators
        int x = 5;
        int y = 5;

        System.out.println(x==y);       // equal operator
        System.out.println(x!=y);       // not equal operator
        System.out.println(x>=y);       // greater and equal operator
        System.out.println(x<=y);       // smaller and equal operator
        System.out.println(x>y);        // bigger operator
        System.out.println(x<y);        // smaller operator
    }
}
